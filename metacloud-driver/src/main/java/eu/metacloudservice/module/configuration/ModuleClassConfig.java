package eu.metacloudservice.module.configuration;

public class ModuleClassConfig {
    public String MODULE_NAME, MODULE_VERSION, MODULE_AUTHOR, MODULE_COPY;

    public ModuleClassConfig(String MODULE_NAME, String MODULE_VERSION, String MODULE_AUTHOR, String MODULE_COPY) {
        this.MODULE_NAME = MODULE_NAME;
        this.MODULE_VERSION = MODULE_VERSION;
        this.MODULE_AUTHOR = MODULE_AUTHOR;
        this.MODULE_COPY = MODULE_COPY;
    }


}
